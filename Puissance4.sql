DROP TABLE IF EXISTS PARTIE;
DROP TABLE IF EXISTS EQUIPE;

CREATE TABLE EQUIPE (
    idEqu INT PRIMARY KEY,
    symbole VARCHAR(50) UNIQUE NOT NULL,
    scoreEqu INT DEFAULT 0
);

